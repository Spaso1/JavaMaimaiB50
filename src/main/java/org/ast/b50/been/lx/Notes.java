package org.ast.b50.been.lx;

public class Notes {
    private int total;
    private int tap;
    private int hold;
    private int slide;
    private int touch;
    private int break_;

    // Getters and Setters
    public int getTotal() {
        return total;
    }

    public void setTotal(int total) {
        this.total = total;
    }

    public int getTap() {
        return tap;
    }

    public void setTap(int tap) {
        this.tap = tap;
    }

    public int getHold() {
        return hold;
    }

    public void setHold(int hold) {
        this.hold = hold;
    }

    public int getSlide() {
        return slide;
    }

    public void setSlide(int slide) {
        this.slide = slide;
    }

    public int getTouch() {
        return touch;
    }

    public void setTouch(int touch) {
        this.touch = touch;
    }

    public int getBreak_() {
        return break_;
    }

    public void setBreak_(int break_) {
        this.break_ = break_;
    }
}
