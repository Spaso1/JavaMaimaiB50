package org.ast.b50.been.lx;

public class Lx_chart {
    private int id;
    private String song_name;
    private String level;
    private int level_index;
    private double achievements;
    private String fc;
    private String fs;
    private int dx_score;
    private double dx_rating;
    private String rate;
    private String type;
    private String upload_time;

    // 无参构造函数
    public Lx_chart() {}

    // 全参构造函数

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getSong_name() {
        return song_name;
    }

    public void setSong_name(String song_name) {
        this.song_name = song_name;
    }

    public String getLevel() {
        return level;
    }

    public void setLevel(String level) {
        this.level = level;
    }

    public int getLevel_index() {
        return level_index;
    }

    public void setLevel_index(int level_index) {
        this.level_index = level_index;
    }

    public double getAchievements() {
        return achievements;
    }

    public void setAchievements(double achievements) {
        this.achievements = achievements;
    }

    public String getFc() {
        return fc;
    }

    public void setFc(String fc) {
        this.fc = fc;
    }

    public String getFs() {
        return fs;
    }

    public void setFs(String fs) {
        this.fs = fs;
    }

    public int getDx_score() {
        return dx_score;
    }

    public void setDx_score(int dx_score) {
        this.dx_score = dx_score;
    }

    public double getDx_rating() {
        return dx_rating;
    }

    public void setDx_rating(double dx_rating) {
        this.dx_rating = dx_rating;
    }

    public String getRate() {
        return rate;
    }

    public void setRate(String rate) {
        this.rate = rate;
    }

    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }

    public String getUpload_time() {
        return upload_time;
    }

    public void setUpload_time(String upload_time) {
        this.upload_time = upload_time;
    }
}
